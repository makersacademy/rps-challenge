require './model/player'

describe Player do

subject(:leo) { Player.new("Leo") }

  describe '#name' do
    it "enters a player's name" do
      expect(leo.name).to eq "Leo"
    end
  end

  describe '#select' do
    it 'allows the player to select a weapon' do
      expect(leo.select("Rock")).to eq "Rock"
    end
  end
end
